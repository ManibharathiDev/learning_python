# Arithmetic Operators

print("Arithmetic Operators")

a = 10
b = 3
# Addition
addition = a + b
# Subtraction
subtraction = a - b
# Multiplication
multiplication = a * b
# Division
division = a / b
# Floor Division
floor_division = a // b 
# Modulus
modulus = a % b
# Exponentiation
exponentiation = a ** b
# Output results
print("Addition:", addition)
print("Subtraction:", subtraction)
print("Multiplication:", multiplication)
print("Division:", division)
print("Floor Division:", floor_division)
print("Modulus:", modulus)
print("Exponentiation:", exponentiation)
# Comparison Operators
print("\nComparison Operators")
x = 5
y = 10
# Equal to
equal_to = x == y
# Not equal to
not_equal_to = x != y
# Greater than
greater_than = x > y
# Less than
less_than = x < y
# Greater than or equal to
greater_than_or_equal_to = x >= y
# Less than or equal to
less_than_or_equal_to = x <= y
# Output results
print("Equal to:", equal_to)
print("Not equal to:", not_equal_to)
print("Greater than:", greater_than)
print("Less than:", less_than)
print("Greater than or equal to:", greater_than_or_equal_to)
print("Less than or equal to:", less_than_or_equal_to)

# Logical Operators
print("\nLogical Operators")
a = True
b = False
# Logical AND
logical_and = a and b
# Logical OR
logical_or = a or b
# Logical NOT
logical_not = not a
# Output results
print("Logical AND:", logical_and)
print("Logical OR:", logical_or)
print("Logical NOT:", logical_not)

# Assignment Operators
print("\nAssignment Operators")
c = 5
# Assign value
d = c
# Add and assign
c += 2
# Subtract and assign
c -= 1
# Multiply and assign
c *= 3
# Divide and assign
c /= 2
# Floor divide and assign
c //= 2
# Modulus and assign
c %= 3
# Exponentiate and assign
c **= 2
# Output results
print("Assigned value:", d)
print("After += 2:", c)
print("After -= 1:", c)
print("After *= 3:", c)
print("After /= 2:", c)
print("After //= 2:", c)
print("After %= 3:", c)
print("After **= 2:", c)

# Identity Operators
print("\nIdentity Operators")
x = [1, 2, 3]
y = x
z = [1, 2, 3]
# is operator
is_same = y is z
is_same2 = y is y
print("y is z:", is_same)
print("y is y:", is_same2)

